package com.monotonic.collections;

public class CollectionBehaviors {

    /*
    size() - Get the number of elements in the collection.
    isEmpty() - Returns true if the collection is empty, false otherwise.
    add() - Add an element to the collection.
    addAll() - Add all elements from one collection to another.
     */

    /*
    remove(element) - Remove the element from the collection.
    removeAll(collection) - Remove all elements from the collection that are in the given collection.
    retainAll(collection) - Remove all elements from the collection that are not in the argument collection.
     */

    /*
    contains(element) - Returns true if the collection contains the element, false otherwise.
    containsAll(collection) - Returns true if the collection contains all the elements in the argument collection, false otherwise.
    clear() - Remove all elements from the collection.
     */

}
